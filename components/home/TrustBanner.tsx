export default function TrustBanner() {
  const [items, setItems] = useState<UspItem[]>(FALLBACK_ITEMS);
  const [active, setActive] = useState(true);

  useEffect(() => {
    fetch('/api/shop-content?section=usps')
      .then((r) => (r.ok ? r.json() : null))
      .then((d) => {
        if (d?.items && Array.isArray(d.items) && d.items.length > 0) {
          setItems(d.items);
        }
        if (d?.is_active === false) setActive(false);
      })
      .catch(() => {});
  }, []);

  if (!active) return null;

  return (
    <section className="bg-brand-black dark:bg-gray-900 text-white py-14" aria-labelledby="trust-heading">
      <div className="max-w-7xl mx-auto px-4 sm:px-6 lg:px-8">
        <h2 id="trust-heading" className="sr-only">Unsere Vorteile</h2>
        <div className={`grid grid-cols-2 gap-8 ${items.length <= 4 ? 'lg:grid-cols-4' : 'lg:grid-cols-' + Math.min(items.length, 6)} lg:gap-4`}>
          {items.map((item, index) => (
            <div
              key={`${item.icon}-${index}`}
              className={`flex flex-col items-center text-center gap-3 ${
                index < items.length - 1 ? 'lg:border-r lg:border-white/10' : ''
              }`}
            >
              <div className="w-12 h-12 rounded-xl bg-white/10 flex items-center justify-center text-cyan-400">
                <UspIcon icon={item.icon} />
              </div>
              <span className="font-body text-sm text-white/80 font-medium">{item.text}</span>
            </div>
          ))}
        </div>
      </div>
    </section>
  );
}
